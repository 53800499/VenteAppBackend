        '| `GET /` | `dashboard:read` (+ `dashboard:financial` pour section financière) |',
        '',
        '### Module 3 — Inventaire',
        '**Catégories** (`/api/categories`)',
        '| Route | Permission |',
        '|-------|------------|',
        '| `GET /` | `inventory:read` |',
        '| `POST /` | `inventory:write` |',
        '| `PATCH :id` | `inventory:write` |',
        '| `DELETE :id` | `inventory:write` |',
        '',
        '**Produits** (`/api/products`)',
        '| Route | Permission |',
        '|-------|------------|',
        '| `GET /` | `inventory:read` |',
        '| `GET low-stock` | `inventory:read` |',
        '| `GET :id` | `inventory:read` |',
        '| `POST /` | `inventory:write` |',
        '| `PATCH :id` | `inventory:write` |',
        '| `PATCH :id/archive` | `inventory:archive` |',
        '| `DELETE :id` | `inventory:archive` |',
        '| `POST :id/stock-adjustments` | `inventory:adjust` |',
        '| `GET :id/stock-movements` | `inventory:read` |',
        '',
        '### Module 4 — Ventes (`/api/sales`)',
        '| Route | Permission |',
        '|-------|------------|',
