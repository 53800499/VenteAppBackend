        '| `GET :id` | `sales:read` |',
        '| `POST /` | `sales:create` (vente standard UC-05) |',
        '| `POST /quick` | `sales:create` (vente rapide RG-VTE-09) |',
        '| `PATCH :id/cancel` | `sales:cancel` (patron, UC-07) |',
        '',
        '### Module 5 — Paiements (`/api/payments`)',
