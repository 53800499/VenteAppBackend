        '| `GET lock-screen/:shopId` | — |',
        '| `POST pin/login` | — |',
        '| `POST refresh` | — |',
        '| `POST logout` | Bearer JWT |',
        '| `POST switch-shop` | `shops:switch` (patron) |',
        '| `GET devices` | Bearer JWT (`?all=true` → `users:read`) |',
        '| `DELETE devices/:id` | Bearer JWT (propre session ou `users:read`) |',
        '| `POST setup` | — |',
        '| `POST emergency-unlock` | — |',
        '| `POST biometric/enable` | `auth:biometric:enable` |',
        '| `POST session/touch` | `auth:session:touch` |',
        '',
        '### Module Multi-boutiques (`/api/shops`)',
        '| Route | Permission |',
        '|-------|------------|',
        '| `GET /` | `shops:read` |',
        '| `GET :id` | `shops:read` |',
        '| `POST /` | `shops:create` |',
        '| `PATCH :id` | `shops:update` |',
        '| `PATCH :id/deactivate` | `shops:deactivate` |',
        '| `POST :id/set-default` | `shops:switch` |',
        '',
        '### Utilisateurs (`/api/users`)',
        '| Route | Permission |',
        '|-------|------------|',
        '| `GET /` | `users:read` |',
        '| `GET :id/assignment` | `users:read` |',
        '| `POST /` | `users:create` |',
        '| `PATCH :id/role` | `users:update_role` |',
        '| `PATCH :id/shop` | `users:assign_shop` |',
        '| `PATCH :id/deactivate` | `users:deactivate` |',
        '',
        '### RBAC (`/api/rbac`)',
        '| Route | Permission |',
        '|-------|------------|',
        '| `GET roles` | `rbac:read` |',
        '| `GET roles/:code` | `rbac:read` |',
        '| `POST roles` | `rbac:manage` |',
        '| `PATCH roles/:code` | `rbac:manage` |',
        '| `DELETE roles/:code` | `rbac:manage` |',
        '| `PUT roles/:code/permissions` | `rbac:manage` |',
        '| `GET permissions` | `rbac:read` |',
        '| `GET me` | session |',
        '| `GET check/:permission` | session |',
        '| `GET users/:userId/overrides` | `rbac:override` |',
